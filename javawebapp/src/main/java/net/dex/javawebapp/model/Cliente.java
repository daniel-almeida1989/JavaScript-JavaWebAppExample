package net.dex.javawebapp.model;

/**
 * Model for Cliente object and its data.
 */
public class Cliente
{
  /** cliente's e-mail. */
  private String email;

  /**
   * Get the cliente's email.
   * @return the cliente's email.
   */
  public String getEmail()
  {
    return this.email;
  }

  /**
   * Set the cliente's email.
   * @param email the cliente's email.
   */
  public void setEmail(String email)
  {
    this.email = email;
  }

}
